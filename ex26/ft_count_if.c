int	ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int cp;

	i = 0;
	cp = 0;
	while (tab[i])
	{
		if ((*f)(tab[i]) == 1)
			cp++;
		i++;
	}
	return (cp);
}
