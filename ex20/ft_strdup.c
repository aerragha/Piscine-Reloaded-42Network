#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*ch;
	int		i;

	ch = malloc(sizeof(*src));
	i = 0;
	while (src[i] != '\0')
	{
		ch[i] = src[i];
		i++;
	}
	ch[i] = '\0';
	return (ch);
}
